#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	j;
	int		k;

	k = 0;
	j = (long)n;
	if (j < 0)
	{
		ft_putchar_fd('-', fd);
		j *= -1;
	}
	k = '0' + (j % 10);
	j /= 10;
	if (j > 0)
		ft_putnbr_fd(j, fd);
	ft_putchar_fd(k, fd);
}
